
namespace JCodes.Framework.jCodesenum.BaseEnum
{
    /// <summary>
    /// An enum which represents all supported graphic formats. These enum names are also used
    /// to get a description from the resources.
    /// </summary>
    public enum ImageFormatTypes
    {
        /// <summary>
        /// Undefined format
        /// </summary>
        imgNone,
        /// <summary>
        /// Windows bitmap
        /// </summary>
        imgBMP,
        /// <summary>
        /// Enhanced Windows metafile
        /// </summary>
        imgEMF,
        /// <summary>
        /// Exchangeable Image File
        /// </summary>
        imgEXIF,
        /// <summary>
        /// Graphics Interchange Format
        /// </summary>
        imgGIF,
        /// <summary>
        /// Windows icon
        /// </summary>
        imgICON,
        /// <summary>
        /// Joint Photographic Experts Group
        /// </summary>
        imgJPEG,
        /// <summary>
        /// Portable Network Graphics
        /// </summary>
        imgPNG,
        /// <summary>
        /// Tag Image File
        /// </summary>
        imgTIFF,
        /// <summary>
        /// Windows metafile
        /// </summary>
        imgWMF
    };
}
